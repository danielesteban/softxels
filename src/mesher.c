#include <math.h>

int edgeTable[256];
int triTable[256][16];

typedef struct {
  const unsigned char value;
  const unsigned char r;
  const unsigned char g;
  const unsigned char b;
} Voxel;

typedef struct {
  const unsigned char x;
  const unsigned char y;
  const unsigned char z;
  const Voxel* voxel;
} Point;

typedef struct {
  unsigned char x;
  unsigned char y;
  unsigned char z;
  unsigned char r;
  unsigned char g;
  unsigned char b;
} Vertex;

typedef struct {
  struct {
    unsigned char x;
    unsigned char y;
    unsigned char z;
  } min;
  struct {
    unsigned char x;
    unsigned char y;
    unsigned char z;
  } max;
} Box;

static const Point get(
  const Voxel* chunks,
  const unsigned char chunkSize,
  const unsigned char x,
  const unsigned char y,
  const unsigned char z
) {
  int chunkX = 0;
  int voxelX = x;
  if (x >= chunkSize) {
    chunkX = 1;
    voxelX -= chunkSize;
  }
  int chunkY = 0;
  int voxelY = y;
  if (y >= chunkSize) {
    chunkY = 1;
    voxelY -= chunkSize;
  }
  int chunkZ = 0;
  int voxelZ = z;
  if (z >= chunkSize) {
    chunkZ = 1;
    voxelZ -= chunkSize;
  }
  const unsigned int offset = (chunkZ * 4 + chunkY * 2 + chunkX) * chunkSize * chunkSize * chunkSize;
  const Point point = { x, y, z, &chunks[offset + voxelZ * chunkSize * chunkSize + voxelY * chunkSize + voxelX] };
  return point;
}

static void growBox(
  Box* box,
  const Vertex* vertex
) {
  if (box->min.x > vertex->x) box->min.x = vertex->x;
  if (box->min.y > vertex->y) box->min.y = vertex->y;
  if (box->min.z > vertex->z) box->min.z = vertex->z;
  if (box->max.x < vertex->x) box->max.x = vertex->x;
  if (box->max.y < vertex->y) box->max.y = vertex->y;
  if (box->max.z < vertex->z) box->max.z = vertex->z;
}

static void interpolate(
  Vertex* vertex,
  const Point* p1,
  const Point* p2,
  const unsigned char isolevel
) {
  const float step = ((float) isolevel - (float) p1->voxel->value) / ((float) p2->voxel->value - (float) p1->voxel->value);
  vertex->x = round((float) p1->x + step * ((float) p2->x - (float) p1->x));
  vertex->y = round((float) p1->y + step * ((float) p2->y - (float) p1->y));
  vertex->z = round((float) p1->z + step * ((float) p2->z - (float) p1->z));
  vertex->r = round((float) p1->voxel->r + step * ((float) p2->voxel->r - (float) p1->voxel->r));
  vertex->g = round((float) p1->voxel->g + step * ((float) p2->voxel->g - (float) p1->voxel->g));
  vertex->b = round((float) p1->voxel->b + step * ((float) p2->voxel->b - (float) p1->voxel->b));
}

const unsigned int run(
  Box* bounds,
  const Voxel* chunks,
  unsigned char* vertices,
  const unsigned char chunkSize,
  const unsigned char isolevel
) {
  bounds->min.x = bounds->min.y = bounds->min.z = chunkSize;
  bounds->max.x = bounds->max.y = bounds->max.z = 0;
  unsigned int offset = 0;
  unsigned int triangles = 0;
  for (unsigned char z = 0; z < chunkSize; z++) {
    for (unsigned char y = 0; y < chunkSize; y++) {
      for (unsigned char x = 0; x < chunkSize; x++) {
        int cubeindex = 0;
        const Point points[8] = {
          get(chunks, chunkSize, x, y, z),
          get(chunks, chunkSize, x, y + 1, z),
          get(chunks, chunkSize, x + 1, y + 1, z),
          get(chunks, chunkSize, x + 1, y, z),
          get(chunks, chunkSize, x, y, z + 1),
          get(chunks, chunkSize, x, y + 1, z + 1),
          get(chunks, chunkSize, x + 1, y + 1, z + 1),
          get(chunks, chunkSize, x + 1, y, z + 1)
        };
        if (points[0].voxel->value <= isolevel) cubeindex |= 1;
        if (points[1].voxel->value <= isolevel) cubeindex |= 2;
        if (points[2].voxel->value <= isolevel) cubeindex |= 4;
        if (points[3].voxel->value <= isolevel) cubeindex |= 8;
        if (points[4].voxel->value <= isolevel) cubeindex |= 16;
        if (points[5].voxel->value <= isolevel) cubeindex |= 32;
        if (points[6].voxel->value <= isolevel) cubeindex |= 64;
        if (points[7].voxel->value <= isolevel) cubeindex |= 128;

        if (edgeTable[cubeindex] == 0) {
          continue;
        }

        Vertex vertlist[12];
        if (edgeTable[cubeindex] & 1)
          interpolate(&vertlist[0], &points[0], &points[1], isolevel);
        if (edgeTable[cubeindex] & 2)
          interpolate(&vertlist[1], &points[1], &points[2], isolevel);
        if (edgeTable[cubeindex] & 4)
          interpolate(&vertlist[2], &points[2], &points[3], isolevel);
        if (edgeTable[cubeindex] & 8)
          interpolate(&vertlist[3], &points[3], &points[0], isolevel);
        if (edgeTable[cubeindex] & 16)
          interpolate(&vertlist[4], &points[4], &points[5], isolevel);
        if (edgeTable[cubeindex] & 32)
          interpolate(&vertlist[5], &points[5], &points[6], isolevel);
        if (edgeTable[cubeindex] & 64)
          interpolate(&vertlist[6], &points[6], &points[7], isolevel);
        if (edgeTable[cubeindex] & 128)
          interpolate(&vertlist[7], &points[7], &points[4], isolevel);
        if (edgeTable[cubeindex] & 256)
          interpolate(&vertlist[8], &points[0], &points[4], isolevel);
        if (edgeTable[cubeindex] & 512)
          interpolate(&vertlist[9], &points[1], &points[5], isolevel);
        if (edgeTable[cubeindex] & 1024)
          interpolate(&vertlist[10], &points[2], &points[6], isolevel);
        if (edgeTable[cubeindex] & 2048)
          interpolate(&vertlist[11], &points[3], &points[7], isolevel);

        for (int i = 0; triTable[cubeindex][i] != -1; i += 3) {
          for (int v = 0; v < 3; v += 1) {
            const Vertex* vertex = &vertlist[triTable[cubeindex][i + v]];
            vertices[offset++] = vertex->x;
            vertices[offset++] = vertex->y;
            vertices[offset++] = vertex->z;
            vertices[offset++] = vertex->r;
            vertices[offset++] = vertex->g;
            vertices[offset++] = vertex->b;
            growBox(bounds, vertex);
          }
          triangles++;
        }
      }
    }
  }
  return triangles;
}

